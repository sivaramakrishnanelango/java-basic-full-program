package org.episode01;

public class Fifteen {
public static void main(String[] args) {
	
	//typecasting
	//upcasting
	
	int a=35;              // size 4-interface
	
	double d=a;            // size 8-class
	
	System.out.println(d);
	
	
	//downcasting
	
	long l = 32546L;         //size 8
	
	float f = (float)l;      // size 4
	System.out.println(f);
}
}
